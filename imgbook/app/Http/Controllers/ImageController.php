    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store($image, $post_id)
    {

        $path = $image->store('/public/images/tmp');
        $path = explode('public', $path);
        $path = '/storage' . $path[1];

        $i = new Image;
        $i->image = $path;
        $i->post_id = $post_id;
        $i->save();

        return $i;

    }

